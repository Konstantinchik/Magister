<h6 class="text-primary mt-3 mb-2">Что такое ОС</h6>
<p class="mb-3"><strong>Операционная система</strong> — комплекс программ, образующий ядро системного уровня программного обеспечения. Её задачи: управление ресурсами компьютера и организация взаимодействия с пользователем.</p>
<h6 class="text-primary mt-3 mb-2">История Windows</h6>
<p class="mb-3"><ol class='mb-0 mt-2'><li><strong>1985–1995</strong>: Windows 1.0–3.11 — операционная <em>оболочка</em> с графическим интерфейсом поверх MS-DOS.</li><li><strong>1995–2009</strong>: Windows 95, 98, ME, NT, XP, Server 2003, Vista — первые полноценные графические ОС.</li><li><strong>2009–2017</strong>: Windows 7, 8, 10, Mobile — современные ОС.</li></ol></p>
<h6 class="text-primary mt-3 mb-2">Графический интерфейс и объектный подход</h6>
<p class="mb-3">Windows построена на принципе <strong>объекта</strong>: объект объединяет данные и действия (алгоритмы) над ними. <strong>Графический интерфейс</strong> — пользователь взаимодействует с компьютером через графические элементы (окна, кнопки, иконки), а не через язык команд.</p>

