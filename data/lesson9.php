<h6 class="text-primary mt-3 mb-2">Что такое алгоритм</h6>
<p class="mb-3"><strong>Алгоритм</strong> — последовательность действий, которая приводит к решению поставленной задачи. Слово произошло от имени математика аль-Хорезми (783–850). Он разработал правила выполнения четырёх арифметических действий.</p>
<h6 class="text-primary mt-3 mb-2">Шесть свойств алгоритма</h6>
<p class="mb-3"><ol class='mb-0 mt-2'><li><strong>Дискретность</strong> — действия записаны по порядку, как отдельные команды.</li><li><strong>Определённость</strong> — каждая команда чёткая и однозначная.</li><li><strong>Понятность</strong> — исполнитель понимает каждую команду.</li><li><strong>Результативность</strong> — алгоритм за конечное число шагов даёт результат или останавливается.</li><li><strong>Массовость</strong> — решает множество однотипных задач.</li><li><strong>Эффективность</strong> — использует минимум действий.</li></ol></p>
<h6 class="text-primary mt-3 mb-2">Исполнитель и способы записи</h6>
<p class="mb-3"><strong>Исполнитель</strong> — это система (человек, группа людей, устройство), которая понимает и выполняет команды алгоритма. В информатике универсальный исполнитель — компьютер. Способы записи алгоритма: <em>словесный</em>, <em>графический</em> (блок-схема), <em>псевдокоды</em>, <em>язык программирования</em>.</p>

