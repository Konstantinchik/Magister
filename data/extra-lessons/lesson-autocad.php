            <p>
                Подпишите элементы интерфейса в своём конспекте или рабочем листе:
                рабочая область, командная строка, слои (Layers), панель инструментов «Главная»,
                строка состояния.
            </p>

            <h6 class="fw-bold mt-3">Задание 2. Построение простого чертежа</h6>
            <ol>
                <li>Постройте квадрат со стороной 50 мм.</li>
                <li>Постройте окружность радиусом 25 мм внутри квадрата.</li>
                <li>Используйте команды: <em>Line / Rectangle / Circle</em>.</li>
                <li>Сделайте скриншот результата и сохраните его для отчёта.</li>
            </ol>

            <h6 class="fw-bold mt-3">Задание 3. Редактирование</h6>
            <p>
                Примените не менее трёх команд редактирования:
                <em>Move, Copy, Offset, Trim, Erase</em>. Модифицируйте исходную фигуру,
                получив две окружности и две копии фигуры.
            </p>

            <h6 class="fw-bold mt-3">Задание 4. Привязки (OSNAP)</h6>
            <p>
                Включите объектные привязки (OSNAP) и соедините объекты точно по вершинам.
                Отметьте активные привязки: Endpoint, Midpoint, Center, Intersection, Quadrant.
            </p>

            <h6 class="fw-bold mt-3">Задание 5. Сохранение и экспорт</h6>
            <ol>
                <li>Сохраните чертеж в формате <strong>DWG</strong> в папку курса.</li>
                <li>Экспортируйте чертеж в <strong>PDF</strong> или <strong>PNG</strong>.</li>
                <li>Проверьте, что файл открывается на другом компьютере.</li>
            </ol>
