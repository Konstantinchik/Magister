    <script>
    function checkAllYesNo() {
        document.querySelectorAll('.yesno-question').forEach(block => {
            const correct = block.dataset.answer;
            const selected = block.querySelector('input[type=radio]:checked');
            const fb = block.querySelector('.feedback');
            fb.classList.add('d-none');
            fb.classList.remove('text-success', 'text-danger');

            if (selected && selected.value === correct) {
                fb.textContent = 'Правильно!';
                fb.classList.add('text-success');
            } else if (selected) {
                fb.textContent = 'Неправильно. Правильный ответ: ' + correct;
                fb.classList.add('text-danger');
            } else {
                fb.textContent = 'Выберите ответ';
                fb.classList.add('text-warning');
            }
            fb.classList.remove('d-none');
        });
    }
