<h6 class="text-primary mt-3 mb-2">Что такое обработка информации</h6>
<p class="mb-3"><strong>Обработка информации</strong> — преобразование одних информационных объектов в другие при выполнении алгоритмов. <strong>Решение задачи на компьютере</strong> — это автоматическое преобразование информации, совместная деятельность человека и компьютера.</p>
<h6 class="text-primary mt-3 mb-2">Шесть этапов решения задачи</h6>
<p class="mb-3"><ol class='mb-0 mt-2'><li><strong>Постановка задачи</strong> — что дано, ограничения, что найти.</li><li><strong>Математическое моделирование</strong> — анализ задач, методов, условий.</li><li><strong>Алгоритмизация</strong> — выбор способа записи, набор команд, порядок.</li><li><strong>Программирование</strong> — кодирование алгоритма на языке программирования.</li><li><strong>Тестирование и отладка</strong> — проверка на ошибки.</li><li><strong>Анализ результатов</strong> — описание полученных результатов.</li></ol></p>
<h6 class="text-primary mt-3 mb-2">Кто что делает</h6>
<p class="mb-3">Задача компьютера — автоматически выполнять команды по алгоритму. Всё остальное (постановка задачи, моделирование, написание алгоритма и программы, проверка) — задача человека.</p>

