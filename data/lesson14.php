<p class="mb-3"><strong>Растровый редактор</strong> работает с изображениями, состоящими из <em>точек (пикселей)</em>, каждая со своими яркостью и цветом. Хороши для фотографий и полиграфии. Примеры: Paint, Adobe Photoshop, GIMP.</p>
<h6 class="text-primary mt-3 mb-2">Векторные редакторы</h6>
<p class="mb-3"><strong>Векторный редактор</strong> работает с <em>линиями</em> (математическими кривыми). Изображение описывается формулами. Подходит для чертежей, логотипов, рекламы. Примеры: CorelDraw, Adobe Illustrator, Inkscape.</p>
<h6 class="text-primary mt-3 mb-2">3D-редакторы</h6>
<p class="mb-3"><strong>3D-редактор</strong> работает с трёхмерными моделями: цвет поверхности, прозрачность, отражения, освещение. Используются в кино, играх, дизайне. Примеры: 3ds Max, Blender, Maya.</p>

