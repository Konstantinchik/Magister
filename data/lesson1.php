    <script>
    function checkTask2() {
        const inputs = document.querySelectorAll('#task2-table .task2-input');
        let correctCount = 0;
        const total = inputs.length;

        inputs.forEach(input => {
            const userAnswer = input.value.trim().toLowerCase()
                .replace(/^это\s+/, '')
                .replace(/[.!?,]/g, '')
                .replace(/\s+/g, ' ');
            const correct = (input.dataset.correct || '').toLowerCase();
            const alt = (input.dataset.alt || '').toLowerCase();
            const fb = input.closest('td').querySelector('.feedback');

            input.classList.remove('is-valid', 'is-invalid');
            fb.classList.remove('d-none', 'text-success', 'text-danger', 'text-warning');

            if (userAnswer === '') {
                fb.textContent = 'Введите ответ';
                fb.classList.add('text-warning');
            } else if (userAnswer === correct || (alt && userAnswer === alt)) {
                input.classList.add('is-valid');
                fb.textContent = 'Правильно';
                fb.classList.add('text-success');
                correctCount++;
            } else {
                input.classList.add('is-invalid');
                fb.innerHTML = 'Правильно: <em>' + correct + '</em>';
                fb.classList.add('text-danger');
            }
        });

        const result = document.getElementById('task2-result');
        result.classList.remove('d-none');
        if (correctCount === total) {
            result.innerHTML = '<div class="alert alert-success text-center"><h5 class="mb-0">Отлично! Все ответы верные!</h5></div>';
        } else {
            result.innerHTML = '<div class="alert alert-info text-center"><h6 class="mb-0">Правильно: ' + correctCount + ' из ' + total + '. Исправь и нажми «Проверить» ещё раз.</h6></div>';
        }
    }
    </script>

