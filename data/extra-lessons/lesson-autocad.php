<script>
// мини-тест (если нужно, можно расширить)
const acTestOptions = ["LINE", "CIRCLE", "MOVE", "COPY", "TRIM", "OFFSET", "OSNAP"];

function acShuffle(arr){
    const a = arr.slice();
    for(let i=a.length-1;i>0;i--){
        const j = Math.floor(Math.random()* (i+1));
        [a[i],a[j]] = [a[j],a[i]];
    }
    return a;
}

document.addEventListener("DOMContentLoaded", () => {
    document.querySelectorAll(".ac-test-select").forEach(sel => {
        sel.innerHTML = "<option value=''>— выберите команду —</option>";
        const shuffled = acShuffle(acTestOptions);
        shuffled.forEach(cmd => {
            const opt = document.createElement("option");
            opt.value = cmd;
            opt.textContent = cmd;
            sel.appendChild(opt);
        });
    });
});

function checkAutocadTest(){
    const selects = document.querySelectorAll(".ac-test-select");
    let correct = 0;
    const total = selects.length;

    selects.forEach(sel => {
        sel.classList.remove("is-valid","is-invalid");
        if(!sel.value) return;
        if(sel.value === sel.dataset.correct){
            sel.classList.add("is-valid");
            correct++;
        } else {
            sel.classList.add("is-invalid");
        }
    });

    const status = document.getElementById("ac-test-status");
    if(!status) return;
    status.innerHTML = `
        <div class="alert alert-info text-center">
            Правильных ответов: ${correct} из ${total}.
        </div>`;
}
