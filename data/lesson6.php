/* Перемешивание вариантов (кроме первого "— выберите —") */
(function shuffleL6Task8Options(){
  const selects = document.querySelectorAll('#l6task8 .l6q8-select');
  selects.forEach(sel => {
    const opts = Array.from(sel.querySelectorAll('option'));
    if (opts.length <= 2) return;

    const first = opts.shift();               // placeholder
    const rest = opts;

    for (let i = rest.length - 1; i > 0; i--) { // Fisher–Yates
      const j = Math.floor(Math.random() * (i + 1));
      [rest[i], rest[j]] = [rest[j], rest[i]];
    }

    sel.innerHTML = '';
    sel.appendChild(first);
    rest.forEach(o => sel.appendChild(o));
  });
})();

/* Проверка задания */
function checkL6Task8() {
  const selects = document.querySelectorAll('#l6task8 .l6q8-select');
  let answered = 0;
  let correct = 0;

  selects.forEach((sel, idx) => {
    const n = idx + 1;
    const res = document.getElementById('l6q8_res' + n);
    const val = (sel.value || '').trim();
    const ok = (sel.dataset.correct || '').trim();

    // сброс
    if (res) {
      res.className = 'text-center';
      res.textContent = '';
    }
    sel.classList.remove('is-valid', 'is-invalid');

    if (!val) return;

    answered++;
    if (val === ok) {
      correct++;
      sel.classList.add('is-valid');
      if (res) {
        res.textContent = '✔';
        res.classList.add('text-success', 'fw-bold');
      }
    } else {
      sel.classList.add('is-invalid');
      if (res) {
        res.textContent = '✘';
        res.classList.add('text-danger', 'fw-bold');
      }
    }
  });

  const status = document.getElementById('l6q8_status');
  if (!status) return;

  if (answered === 0) {
    status.innerHTML = `<div class="alert alert-warning text-center mb-0">
      Выберите ответы хотя бы в нескольких пунктах, затем нажмите «Проверить».
    </div>`;
    return;
  }

  if (correct === selects.length) {
    status.innerHTML = `<div class="alert alert-success text-center mb-0">
      Отлично! Все ответы верные: ${correct} из ${selects.length}.
    </div>`;
  } else {
    status.innerHTML = `<div class="alert alert-info text-center mb-0">
      Верно: ${correct} из ${selects.length}. Можно исправить неверные ответы и проверить ещё раз.
    </div>`;
  }

  window.__scrollToCheck(status);
}
