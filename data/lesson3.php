<script>
// перемешивание массива (Фишер–Йетс)
function shuffleArray(arr) {
    const a = arr.slice();
    for (let i = a.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [a[i], a[j]] = [a[j], a[i]];
    }
    return a;
}

// инициализация: заполняем select'ы и перемешиваем ответы
function initL3Task10() {
    const selects = document.querySelectorAll('.l3t10');

    selects.forEach(sel => {
        const raw = sel.dataset.answers;
        if (!raw) return;

        let answers;
        try {
            answers = JSON.parse(raw);
        } catch (e) {
            console.error('Ошибка в data-answers:', raw, e);
            return;
        }

        if (!Array.isArray(answers) || answers.length === 0) return;

        const correct = answers[0];          // первый — правильный
        sel.dataset.correct = correct;       // запомним его отдельно

        const shuffled = shuffleArray(answers);

        // очищаем и заполняем select
        sel.innerHTML = '';

        const placeholder = document.createElement('option');
        placeholder.value = '';
        placeholder.textContent = '— выберите —';
        placeholder.selected = true;
        placeholder.disabled = true;
        sel.appendChild(placeholder);

        shuffled.forEach(text => {
            const opt = document.createElement('option');
            opt.value = text;
            opt.textContent = text;
            sel.appendChild(opt);
        });
    });
}

// проверка
function checkL3Task10() {
    const selects = document.querySelectorAll('.l3t10');
    let correctCount = 0;

    selects.forEach(sel => {
        sel.classList.remove('is-valid', 'is-invalid');

        const value = sel.value;
        const correct = sel.dataset.correct;

        if (!value) return; // пропускаем пустые

        if (value === correct) {
            sel.classList.add('is-valid');
            correctCount++;
        } else {
            sel.classList.add('is-invalid');
        }
    });

    const status = document.getElementById('l3task10-status');
    const total = selects.length;

    if (correctCount === total) {
        status.innerHTML = `
            <div class="alert alert-success text-center">
                Отлично! Все ${correctCount} ответов правильные.
            </div>`;
    } else if (correctCount === 0) {
        status.innerHTML = `
            <div class="alert alert-warning text-center">
                Пока нет правильных ответов. Попробуйте ещё раз.
            </div>`;
    } else {
        status.innerHTML = `
            <div class="alert alert-info text-center">
                Правильных ответов: ${correctCount} из ${total}.
            </div>`;
    }

    window.__scrollToCheck(status);
}

