<script>
function checkTask17() {
  const wrap = document.getElementById('task17');
  const inputs = wrap.querySelectorAll('input[data-answer]');
  let done = 0, correct = 0;

  inputs.forEach(inp => {
    const fb = inp.nextElementSibling; // .feedback
    const userRaw = (inp.value || '').trim();
    const user = userRaw.replace(/\s+/g, '').replace(/^0+/, ''); // убираем пробелы/ведущие нули
    const ans  = (inp.dataset.answer || '').trim();

    fb.classList.remove('text-success', 'text-danger');
    inp.classList.remove('is-valid', 'is-invalid');

    if (!userRaw) {
      fb.textContent = '';
      return;
    }

    done++;

    // допускаем ввод вроде "1 0110 0"
    if (/^[01]+$/.test(user) && user === ans) {
      correct++;
      fb.textContent = 'Верно';
      fb.classList.add('text-success');
      inp.classList.add('is-valid');
    } else {
      fb.textContent = 'Проверьте (только 0 и 1)';
      fb.classList.add('text-danger');
      inp.classList.add('is-invalid');
    }
  });

  const status = document.getElementById('task17-status');
  if (done === 0) {
    status.innerHTML = `<div class="alert alert-info text-center mb-0">Введите ответы хотя бы для одного числа.</div>`;
    return;
  }

  if (correct === inputs.length) {
    status.innerHTML = `<div class="alert alert-success text-center mb-0">Отлично! Все ответы верные (${correct} из ${inputs.length}).</div>`;
  } else {
    status.innerHTML = `<div class="alert alert-info text-center mb-0">Верно: ${correct} из ${inputs.length}. Исправьте отмеченные поля.</div>`;
  }

  window.__scrollToCheck(status);
}
</script>
