document.addEventListener('DOMContentLoaded', () => {

  function shuffle(arr) {
    // Fisher–Yates
    for (let i = arr.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [arr[i], arr[j]] = [arr[j], arr[i]];
    }
    return arr;
  }

  document.querySelectorAll('select[data-correct][data-options]').forEach(sel => {
    let options;
    try {
      options = JSON.parse(sel.dataset.options);
    } catch (e) {
      options = [];
    }

    // Перемешиваем 5 вариантов (placeholder отдельно)
    shuffle(options);

    // Очищаем и добавляем placeholder
    sel.innerHTML = '';
    const ph = document.createElement('option');
    ph.value = '';
    ph.textContent = '— выберите —';
    sel.appendChild(ph);

    // Добавляем 5 вариантов
    options.slice(0, 5).forEach(text => {
      const opt = document.createElement('option');
      opt.value = text;
      opt.textContent = text;
      sel.appendChild(opt);
    });
  });

});
