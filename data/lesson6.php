/* Нормализация: убираем ударения и лишние пробелы */
function l6Normalize(str) {
  return (str || '')
    .toLowerCase()
    .replace(/[́̀]/g, '')         // знаки ударения (комбинируемые)
    .replace(/ё/g, 'е')
    .replace(/[^\p{L}\p{N}\s-]/gu, '') // лишнюю пунктуацию
    .replace(/\s+/g, ' ')
    .trim();
}

function l6Task9FillExample() {
  const ta = document.getElementById('l6task9Input');
  if (!ta) return;
  if (!ta.value.trim()) ta.value = 'аналоговая информация';
  ta.focus();
}

function l6Task9AddLine(text) {
  const ta = document.getElementById('l6task9Input');
  if (!ta) return;
  const current = ta.value.trim();
  const add = text;
  ta.value = current ? (current + "\n" + add) : add;
  ta.focus();
}

/* Мягкая проверка: считаем, сколько определений найдено */
function checkL6Task9() {
  const ta = document.getElementById('l6task9Input');
  const status = document.getElementById('l6task9Status');
  if (!ta || !status) return;

  const allowed = [
    'аналоговая информация',
    'цифровая информация',
    'непрерывная информация',
    'дискретная информация',
    'символьная информация',
    'числовая информация',
    'графическая информация',
    'звуковая информация',
    'видеоинформация' // в тексте идёт слитно
  ];

  const allowedSet = new Set(allowed.map(l6Normalize));

  const lines = ta.value
    .split('\n')
    .map(s => s.trim())
    .filter(Boolean);

  if (!lines.length) {
    status.innerHTML = `<div class="alert alert-warning mb-0">
      Напишите хотя бы 2–3 примера (например: «аналоговая информация», «цифровая информация»).
    </div>`;
    return;
  }

  const found = new Set();
  const unknown = [];

  lines.forEach(line => {
    const n = l6Normalize(line);
    if (allowedSet.has(n)) found.add(n);
    else unknown.push(line);
  });

  const missing = allowed.filter(x => !found.has(l6Normalize(x)));

  status.innerHTML = `
    <div class="alert alert-info mb-0">
      <div class="fw-bold">Результат:</div>
      <div>Найдено вариантов: <strong>${found.size}</strong> из ${allowed.length} (по списку подсказки).</div>

      ${unknown.length ? `
        <hr class="my-2">
        <div class="fw-bold">Строки, которых нет в списке подсказки (возможно, вы взяли их из другого места — это не ошибка):</div>
        <ul class="mb-0">
          ${unknown.slice(0, 8).map(u => `<li>${u}</li>`).join('')}
          ${unknown.length > 8 ? `<li>…и ещё ${unknown.length - 8}</li>` : ``}
        </ul>
      ` : ``}

      ${missing.length ? `
        <hr class="my-2">
        <div class="fw-bold">Можно ещё добавить:</div>
        <div class="d-flex flex-wrap gap-2">
          ${missing.map(m => `<span class="badge text-bg-light border">${m}</span>`).join('')}
        </div>
      ` : `
        <hr class="my-2">
        <div class="fw-bold text-success">Отлично! Вы указали все варианты из подсказки.</div>
      `}
    </div>
  `;

  window.__scrollToCheck(status);
}
