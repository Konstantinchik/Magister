        // 1) Если галочка НЕ стоит → ничего не показываем
        // ————————————————————————————
        if (!checkbox.checked) {
            return;
        }

        // ————————————————————————————
        // 2) Если галочка стоит и ответ ПРАВИЛЬНЫЙ
        // ————————————————————————————
        if (checkbox.checked && shouldBeChecked) {
            correctSelected++;
            item.classList.add('border-success', 'bg-success-subtle');
            fb.textContent = 'Правильно!';
