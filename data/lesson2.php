            <div class="alert alert-warning text-center">
                Правильно выбрано: ${correctSelected} из ${mustBeChecked}.
                Ошибок: ${mistakes}. Попробуйте ещё раз.
            </div>
        `;
    }

    window.__scrollToCheck(result);
}

