<script>
document.getElementById('officeTestCheck').addEventListener('click', function () {
    const radios = document.querySelectorAll('.office-q');
    let answered = 0;
    let correct = 0;

    // БЛОК 1
    const grouped = {};
    radios.forEach(r => {
        const name = r.name;
        if (!grouped[name]) grouped[name] = [];
        grouped[name].push(r);
    });

    Object.values(grouped).forEach(group => {
        let chosen = group.find(r => r.checked);
        if (chosen) {
            answered++;
            const correctIndex = chosen.dataset.correct;
            if (chosen.value === correctIndex) {
                correct++;
            }
        }
    });

    const total = Object.keys(grouped).length;
    const percent = Math.round(correct / total * 100);

    let html = `
        <div class="alert alert-info">
            Блок 1: верно ${correct} из ${total} (${percent}%).
        </div>
        <div class="alert alert-secondary">
            Блок 2: ответы с открытым вводом проверяются преподавателем.
        </div>
    `;

    document.getElementById('officeTestResult').innerHTML = html;
    document.getElementById('officeTestResult').scrollIntoView({behavior: 'smooth'});
});
</script>
