    selects.forEach(select => {
        const correct = select.dataset.correct;
        const value = select.value;
        const fb = select.nextElementSibling;

        fb.className = 'feedback small mt-1';
        fb.textContent = '';

        // если студент пока ничего не выбрал — ничего не подсказываем
        if (!value) {
            return;
        }

        if (value === correct) {
            correctCount++;
            fb.textContent = 'Правильно!';
            fb.classList.add('text-success', 'fw-bold');
        } else {
            fb.textContent = 'Неправильно.';
            fb.classList.add('text-danger', 'fw-bold');
        }
    });

    const status = document.getElementById('l2task9-status');

    if (correctCount === total) {
        status.innerHTML = `
            <div class="alert alert-success text-center">
                Отлично! Все соответствия верные (${correctCount} из ${total}).
            </div>`;
    } else {
        status.innerHTML = `
            <div class="alert alert-warning text-center">
                Правильных: ${correctCount} из ${total}. Попробуйте ещё раз.
            </div>`;
    }

    window.__scrollToCheck(status);
}
