<h6 class="text-primary mt-3 mb-2">Текстовый редактор и текстовый процессор</h6>
<p class="mb-3"><strong>Текстовый редактор</strong> (Notepad, Блокнот) — программа для ввода и редактирования текста, без оформления. <strong>Текстовый процессор</strong> (Word) — позволяет ещё и форматировать: менять шрифты, размеры, цвета, добавлять таблицы, рисунки.</p>
<h6 class="text-primary mt-3 mb-2">Редактирование и форматирование</h6>
<p class="mb-3"><strong>Редактирование</strong> — исправление текста, изменение или сохранение смысла. Внешний вид при этом не меняется. <strong>Форматирование</strong> — преобразование внешнего вида и расположения текста. Смысл сохраняется.</p>
<h6 class="text-primary mt-3 mb-2">Microsoft Word</h6>
<p class="mb-3"><strong>Word</strong> — самый известный текстовый процессор. Объект обработки — <em>документ</em>. Word может работать с несколькими документами одновременно: для каждого открывается своё окно.</p>

